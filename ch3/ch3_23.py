x1 = 97
x2 = chr(x1)      
print(x2)               # 輸出數值97的字元
print("========== 我是分隔線 ==========")
x3 = ord(x2)
print(x3)               # 輸出字元x3的Unicode(10進位)碼值
print("========== 我是分隔線 ==========")
x4 = '虎'
print(hex(ord(x4)))     # 輸出字元'虎'的Unicode(16進位)碼值

