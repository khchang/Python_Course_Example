cars = ['honda','bmw','toyota','ford']     
print("目前串列內容 = ",cars)
print("使用sort()由小排到大")
cars.sort()            
print("排序串列結果 = ",cars)
print('========== 我是分隔線 ==========')
nums = [5, 3, 9, 2]
print("目前串列內容 = ",nums)
print("使用sort()由小排到大")
nums.sort()            
print("排序串列結果 = ",nums)
print('========== 我是分隔線 ==========')
print("使用sort()由大排到小")
nums.sort(reverse=True)            
print("排序串列結果 = ",nums)
    
